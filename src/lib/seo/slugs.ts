// Хелпер: генерация slug для МФО
export function generateMfoSlug(name: string): string {
  return name
    .toLowerCase()
    .replace(/[^\wа-яё]/gi, '-')
    .replace(/-+/g, '-')
    .replace(/^-|-$/g, '');
}

// Хелпер: транслитерация
export function transliterate(text: string): string {
  const ru = {
    'а': 'a', 'б': 'b', 'в': 'v', 'г': 'g', 'д': 'd', 'е': 'e', 'ё': 'yo',
    'ж': 'zh', 'з': 'z', 'и': 'i', 'й': 'y', 'к': 'k', 'л': 'l', 'м': 'm',
    'н': 'n', 'о': 'o', 'п': 'p', 'р': 'r', 'с': 's', 'т': 't', 'у': 'u',
    'ф': 'f', 'х': 'h', 'ц': 'ts', 'ч': 'ch', 'ш': 'sh', 'щ': 'sch', 'ъ': '',
    'ы': 'y', 'ь': '', 'э': 'e', 'ю': 'yu', 'я': 'ya',
  };
  
  return text.split('').map(char => ru[char as keyof typeof ru] || char).join('');
}

// Хелпер: генерация slug города
export function generateCitySlug(cityName: string): string {
  return transliterate(cityName.toLowerCase().replace(/\s+/g, '-'));
}

