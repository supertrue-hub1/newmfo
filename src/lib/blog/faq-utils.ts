import type { FAQItem } from '@/contexts/page-faq-context';

/**
 * Утилиты для работы с FAQ в блоге
 */

/**
 * Пример структуры статьи с встроенными FAQ блоками
 * Используется при рендеринге из CMS или MDX
 */
export interface ArticleSection {
  type: 'text' | 'heading' | 'faq' | 'image' | 'quote';
  content?: string;
  level?: 1 | 2 | 3 | 4 | 5 | 6;
  items?: FAQItem[];
  src?: string;
  alt?: string;
  author?: string;
}

/**
 * Извлекает все FAQ из структуры статьи
 */
export function extractFAQsFromSections(sections: ArticleSection[]): FAQItem[] {
  return sections.reduce<FAQItem[]>((acc, section) => {
    if (section.type === 'faq' && section.items) {
      return [...acc, ...section.items];
    }
    return acc;
  }, []);
}

